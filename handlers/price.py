@router.callback_query(F.data == "price")
async def show_price_categories(callback: CallbackQuery):

    await callback.message.edit_text(
        "Выберите категорию:",
        reply_markup=price_categories_keyboard()
    )

    try:
        await callback.answer()
    except TelegramBadRequest:
        pass


